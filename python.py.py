# Base Class
class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def display_person_info(self):
        print(f"Name: {self.name}")
        print(f"Age: {self.age}")


# Derived Class (inherits from Person)
class Employee(Person):
    def __init__(self, name, age, employee_id, salary):
        super().__init__(name, age)
        self.employee_id = employee_id
        self.salary = salary

    def display_employee_info(self):
        print(f"Employee ID: {self.employee_id}")
        print(f"Salary: {self.salary}")


# Manager Class (Multiple Inheritance)
class Manager(Employee):
    def __init__(self, name, age, employee_id, salary, department):
        super().__init__(name, age, employee_id, salary)
        self.department = department

    def display_manager_info(self):
        print(f"Department: {self.department}")

    def manage_team(self):
        print(f"{self.name} is managing the {self.department} department.")


# Creating Manager Object
mgr = Manager("Rahul", 35, "E102", 75000, "IT")

# Accessing methods from all classes
mgr.display_person_info()
mgr.display_employee_info()
mgr.display_manager_info()
mgr.manage_team()